void solve()
{
    long long n, m;
    cin >> n >> m;

    // 2a + b = n
    // 2b + a = m

    if ((m + n) % 3 == 0 && m <= 2 * n && n <= 2 * m)
    {
        cout << "YES" << endl;
    }
    else
    {
        cout << "NO" << endl;
    }
}

